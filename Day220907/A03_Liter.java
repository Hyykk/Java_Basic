package Day220907;

public class A03_Liter {
	public static void main(String[] args) {
		//상수: 절대로 변하지 않는 값(데이터)
		//정수상수, 실수상수, 문자상수, 문자열상수, 주소상수(참조)
		 int a = 500;				//500:  정수상수
		 float b = 3.4f;			//3.3f: 실수상수
		 double c = 3.14f;			//3.14: 실수상수
		 char d = 'X';				//'X' : 문자상수
		 String e = "Java Hello";	//"Java Hello": 문자열 상수
	}
}
